typedef int ElemType;

typedef struct Node{
    ElemType value;
    struct Node *left, *right;
}Node, *NodePtr;

typedef struct BinarySortTree{
    NodePtr root;// = NULL;
} BinarySortTree, *BinarySortTreePtr;


//链式队列结构
typedef struct qnode
{
	NodePtr data;                 //数据域指针
	struct node *next;            //指向当前结点的下一结点
} QNode;

typedef struct Lqueue
{
    QNode *front;                   //队头
    QNode *rear;                    //队尾
    size_t length;                 //队列长度
} LQueue;

/**
 * BST initialize
 * @param BinarySortTreePtr BST
 * @return is complete
 */
